-- Создание базы данных
DROP DATABASE IF EXISTS fruzbuka;
CREATE DATABASE fruzbuka;
USE fruzbuka;

/*
 ******************************* 
 * Создание таблиц базы данных *
 *******************************
*/

-- Таблица categories
DROP TABLE IF EXISTS categories;
CREATE TABLE categories (
  id SERIAL PRIMARY KEY,
  name VARCHAR(255) COMMENT 'Наименование категории'
) COMMENT = 'Категории товаров магазина';

-- Таблица products
DROP TABLE IF EXISTS products;
CREATE TABLE products (
  id SERIAL PRIMARY KEY,
  name VARCHAR(255) COMMENT 'Наименование',
  desсription TEXT COMMENT 'Описание',
  price DECIMAL (11,2) COMMENT 'Цена за единицу/кг',
  cat_id BIGINT UNSIGNED NOT NULL,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  FOREIGN KEY (cat_id) REFERENCES categories(id)
) COMMENT = 'Товарные позиции';

-- Таблица tokens
DROP TABLE IF EXISTS tokens;
CREATE TABLE tokens (
  id SERIAL PRIMARY KEY,
  token VARCHAR(255) COMMENT 'Токен'
) COMMENT = 'Авторизационные токены';

-- Таблица accounts
DROP TABLE IF EXISTS accounts;
CREATE TABLE accounts (
  id SERIAL PRIMARY KEY,
  first_name VARCHAR(255) COMMENT 'Имя',
  last_name VARCHAR(255) COMMENT 'Фамилия',
  phone_number VARCHAR(50) COMMENT 'Номер телефона',
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) COMMENT = 'Учетные записи';

-- Таблица point_sales
DROP TABLE IF EXISTS point_sales;
CREATE TABLE point_sales (
  id SERIAL PRIMARY KEY,
  point_name VARCHAR(255) COMMENT 'Наименование точки продажи',
  point_x INT UNSIGNED COMMENT 'Координата Х точки продажи',
  point_y INT UNSIGNED COMMENT 'Координата Y точки продажи',
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP
) COMMENT = 'Точки продажи';

-- Таблица products_to_point_sales
DROP TABLE IF EXISTS products_to_point_sales;
CREATE TABLE products_to_point_sales (
  id SERIAL PRIMARY KEY,
  point_id BIGINT UNSIGNED NOT NULL,
  product_id BIGINT UNSIGNED NOT NULL,
  total INT UNSIGNED COMMENT 'Общее Кол-во',
  created_at DATETIME,
  FOREIGN KEY (point_id) REFERENCES point_sales(id),
  FOREIGN KEY (product_id) REFERENCES products(id)
) COMMENT = 'Продукты на точке продажи';

-- Таблица accounts_to_tokens
DROP TABLE IF EXISTS accounts_to_tokens;
CREATE TABLE accounts_to_tokens (
  id SERIAL PRIMARY KEY,
  token_id BIGINT UNSIGNED NOT NULL,
  account_id BIGINT UNSIGNED NOT NULL,
  FOREIGN KEY (token_id) REFERENCES tokens(id),
  FOREIGN KEY (account_id) REFERENCES accounts(id)
) COMMENT = 'Авторизационные токены для аккаунтов';

-- Таблица accounts_to_point_sale
DROP TABLE IF EXISTS accounts_to_point_sale;
CREATE TABLE accounts_to_point_sale (
  id SERIAL PRIMARY KEY,
  point_id BIGINT UNSIGNED NOT NULL,
  account_id BIGINT UNSIGNED NOT NULL,
  FOREIGN KEY (point_id) REFERENCES point_sales(id),
  FOREIGN KEY (account_id) REFERENCES accounts(id)
) COMMENT = 'Сотрудники на точках продажи';

-- Таблица roles
DROP TABLE IF EXISTS roles;
CREATE TABLE roles (
  id SERIAL PRIMARY KEY,
  name VARCHAR(255) COMMENT 'Наименование'
) COMMENT = 'Роли сотрудников';

-- Таблица commands
DROP TABLE IF EXISTS commands;
CREATE TABLE commands (
  id SERIAL PRIMARY KEY,
  name VARCHAR(255) COMMENT 'Наименование'
) COMMENT = 'Команды управления';

-- Таблица accounts_to_roles
DROP TABLE IF EXISTS accounts_to_roles;
CREATE TABLE accounts_to_roles (
  id SERIAL PRIMARY KEY,
  account_id BIGINT UNSIGNED NOT NULL,
  role_id BIGINT UNSIGNED NOT NULL,
  FOREIGN KEY (account_id) REFERENCES accounts(id),
  FOREIGN KEY (role_id) REFERENCES roles(id)  
) COMMENT = 'Команды управления для ролей';

-- Таблица commands_to_roles
DROP TABLE IF EXISTS commands_to_roles;
CREATE TABLE commands_to_roles (
  id SERIAL PRIMARY KEY,
  role_id BIGINT UNSIGNED NOT NULL,
  command_id BIGINT UNSIGNED NOT NULL,
  FOREIGN KEY (role_id) REFERENCES roles(id),
  FOREIGN KEY (command_id) REFERENCES commands(id)
) COMMENT = 'Команды управления для ролей';

-- Таблица realized_products
DROP TABLE IF EXISTS realized_products;
CREATE TABLE realized_products (
  id SERIAL PRIMARY KEY,
  product_on_point_id BIGINT UNSIGNED NOT NULL,
  weight_or_count DECIMAL (11,2) COMMENT 'Реализованный товар в кг/единицах',
  total DECIMAL (11,2) COMMENT 'Итоговая выручка',
  vendor_id BIGINT UNSIGNED NOT NULL,
  sold_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (product_on_point_id) REFERENCES products_to_point_sales(id),
  FOREIGN KEY (vendor_id) REFERENCES accounts(id)
) COMMENT = 'Реализованный товар';



